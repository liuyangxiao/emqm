public class LogUtils {
    private static boolean isLoggable = false;

    public static void init() {
        FormatStrategy formatStrategy = PrettyFormatStrategy.newBuilder()
                .showThreadInfo(true)  // (Optional) Whether to show thread info or not. Default true
                /*   .methodCount(3)   */    // 从打印开始  往回数几个函数(Optional) How many method line to show. Default 2
                /*  .methodOffset(5)     */  // 往上回到第几个函数开始打印 (Optional) Hides internal method calls up to offset. Default 5
                .tag("xx-tag")   // (Optional) Global tag for every log. Default PRETTY_LOGGER
                .build();
        Logger.addLogAdapter(new AndroidLogAdapter(formatStrategy) {
            @Override
            public boolean isLoggable(int priority, String tag) {
                return isLoggable;
            }
        });
    }
}
